4. Airway
Setelah melakukan 30 kompresi, buka jalan nafas korban dengan metode Head-tilt chin-lift. Tujuannya adalah untuk membuka jalan nafas korban yang tersumbat oleh lidah yang tertarik ke tenggorokan sehingga menutupi jalan nafas. Cara melakukan metode Head-tilt chin-liftyaitu :
o Letakkan telapak tangan Anda di dahi korban dan letakkan jari-jari tangan Anda yang lain dibawah dagu korban.
o Kemudian tekan dahi ke bawah sambil angkat dagu keatas sehingga kepala korban mendongak keatas dan mulut korban terbuka.

5. Breathing
Setelah jalan nafas terbuka, lanjutkan dengan pemberian 2 kali nafas bantuan dari mulut ke mulut. Perhatikan membusungnya dada korban untuk memastikan Volume tidal. Volume tidal adalah jumlah udara yang dihirup dan dihembuskan setiap kali bernafas, dimana volume tidal normal sesorang adalah 350-400ml. Adapun cara memberikan nafas bantuan sebagai berikut :
o Pastikan jalan nafas korban masih dalan posisi terbuka dengan metode Head-tilt chin-liftsebelumnya.
o Tekan hidung korban untuk memastikan tidak ada udara yang bocor melalui hidung, ambil nafas dengan normal lalu tempelkan mulut serapat mungkin pada mulut korban dan tiupkan nafas Anda melalui mulut.

Lakukan dengan perbandingan 30:2 yaitu 30 kompresi dada dan 2 kali napas bantuan, sampai ada respon dari korban atau sampai bantuan medis tiba. Perlu diketahui, bahwa otak tidak boleh kekurangan oksigen lebih dari 4 menit terutama saat diketahui jantung seseorang berhenti. Itu artinya Anda hanya punya waktu kurang dari 4 menit untuk melakukan RJP pada korban. </p><br /><br />

