            	<h2>Informasi Mengenai Obesitas</h2>
                <img src="images/Obesity.jpg" align="left" style="padding-right:20px; padding-top:5px;" />
                <p>Obesitas merupakan suatu kondisi dimana kelebihan jaringan adipose pada tubuh manusia. Adipose dalam bahasa sehari-sehari disebut jaringan lemak. Peningkatan akumulasi jaringan lemak ini biasanya tertimbun di dalam tubuh manusia seperti omentum (pelapis dinding dalam perut dan organ dalam), bawah kulit dan jaringan lemak lain yang tersebar diseluruh tubuh manusia. Pengukuran jaringan lemak tubuh dapat dilakukan dengan mengukur BMI (Body Mass Index) yang dapat dihitung berdasarkan rumus sederhana yaitu berat badan (Kilogram) dibagi kuadrat dari tinggi badan dalam meter.

    Dampak obesitas bagi kesehatan cenderung membuat orang tersebut lebih rentan terkena penyakit seperti penyakit jantung koroner, penyakit metabolik, stroke dan lain-sebagainya

Secara fisiologis, perempuan memiliki kadar lemak tubuh lebih banyak daripada laki-laki. Dampak obesitas bagi kesehatan yaitu : cenderung membuat orang tersebut lebih mudah mempunyai faktor resiko terjadinya penyakit seperti penyakit jantung koroner, penyakit metabolik, stroke dan lain-sebagainya.

Klasifikasi Obesitas

Pertama, dengan menggunakan BMI.

Berdasarkan klasifikasi World health Organization (WHO), tingkat lemak dalam tubuh dapat diklasifikasikan menjadi:

Underweight (kekurangan berat) : BMI < 18.50

Normal : BMI 18.50-24.99

Overweight (kelebihan berat) : BMI ≥25.00

Kemudian, underweight di bagi lagi menjadi severe thinness (sangat kurus, yakni BMI < 16.00), moderate thinnes (kurus sedang, yakni BMI 16.00-16.99) dan mild thinness (agak kurus, yakni BMI 17.00-18.49). Sedangkan kelas overweight dibagi lagi menjadi pre-obese (dengan BMI 25.00-29.99), dan obese (dengan BMI ≥ 30.00) .

Selanjutnya, obese sendiri dibagi lagi menjadi obese class I, yakni BMI 30.00-34.99, class 2, yakni BMI 35.00-39.99 dan class III, yakni BMI ≥ 40.00. Obese kelas III ini disebut juga dengan obesitas morbid.  Prinsipnya adalah, range normal nya adalah 18.5-24,99, sehingga bila kita mengukur BMI, langkah selanjutnya adalah melihat range kelas BMI seperti yang telah dikemukakan diatas. Intinya semakin seseorang besar nilai overweight (BMInya) di atas normal maka semakin beresiko seseorang untuk menderita penyakit kardiovaskuler, seperti jantung koroner, stroke, deep venous thrombosis dll. Sehingga sebagai generasi yang sehat, maka hendaknya kita harus dapat mengontrol BMI dalam diri kita.

    Intinya semakin seseorang besar nilai overweight (BMInya) di atas normal maka semakin beresiko seseorang untuk menderita penyakit kardiovaskuler, seperti jantung koroner, stroke, deep venous thrombosis dll

Kedua, dengan menggunakan rasio pinggang dan pinggul (whist and Hip Circumference)

Pengukuran obesitas dapat kita ukur juga dari rasio antara circumferens dari wrist dan hip (rasio antara lingkar pinggang dan pinggul). Bedanya pinggang dan pinggul adalah, kalau pinggang di ukurnya sekitar di atas pusar (umbilicus), sedangkan kalau pinggul di ukurnya di tepi bawah tonjolan samping tulang pinggul atau kita sebut dibawah tonjolan atas tulang panggul (spina iliaca anterior superior) dan diatas tonjolan bawahnya (spina iliaca anterior inferior). Prinsipnya nanti akan didapatkan data dalam satuan yang sama, misal centimeter semua atau meter semua. Kemudian baru di bandingkan rasio antara panggul dan pinggang.

Jika didapat hasil rasio dibawah 0,75 maka hasilnya sangat baik (tidak obese), jika 0,75-0,8 maka baik. Pada kebanyakan orang biasanya didapatkan hasil yaitu 0,8-0,85. Sedangkan bila didapatkan 0,85-0,9 maka termasuk tinggi dan bila di atas 0,9 maka termasuk kondisi extrim obese yang beresiko menjadi faktor risiko dan penyebab dari berbagai penyakit. Antara laki-laki dan perempuan sebenarnya terdapat perbedaan rincian nilai range lingkar tersebut, tetapi perbedaannya tidaklah terlalu signifikan.

Epidemiologi Obesitas

Berdasarkan data yang terhimpun dari National Health and Nutrition Examination Surveys (NHANES), menunjukkan bahwa presentase populasi obesitas di amerika (BMI > 30), meningkat dari 14,5% sejak tahun 1980 menjadi 33,9% pada tahun antara 2007 dan 2008. Hal ini diperparah dengan fakta yang menunjukkan bahwa extreme obesity (BMI>40) mencapai 5,7 % polpulasi. Sedangkan sekitar 68% orang dewasa di amerika dengan usia lebih dari 20 tahun menunjukkan dia termasuk overweight (BMI>25).

Untuk prevalensi overweight dan obesitas juga meningkat sangat tajam di kawasan Asia-Pasifik. Sebagai contoh, 20,5% dari penduduk Korea Selatan tergolong overweight dan 1,5% tergolong obesitas. Di Thailand, 16% penduduknya mengalami overweight dan 4% mengalami obesitas. Di daerah perkotaan Cina, prevalensi overweight adalah 12,% pada laki-laki dan 14,4% pada perempuan, sedang di daerah pedesaan prevalensi overweight  pada laki-laki dan perempuan masing-masing adalah 5,3% dan 9,8%.

Sekarang kita beranjak ke negeri kita tercinta ini, Indonesia. Di Indonesia, angka prevalensi obesitas juga menunjukkan angka yang cukup mengkhawatirkan. Berdasarkan data SUSENAS tahun 2004, prevalensi obesitas pada anak telah mencapai 11%. Berdasarkan data Riset Kesehatan Dasar (Riskesdas) tahun 2007, prevalensi nasional obesitas umum pada penduduk berusia ≥ 15 tahun adalah 10,3% terdiri dari laki-laki 13,9%, dan perempuan 23,8% , sedangkan prevalensi overweight pada anak-anak usia 6-14 tahun pada laki-laki 9,5% dan pada perempuan 6,4%. Angka ini hampir sama dengan estimasi WHO sebesar 10% pada anak usia 5-17 tahun.

Penyebab dan yang berisiko mengalami Obesitas

Ada beberapa penyemab yang harus diketahui yang dapat menimbulkan obesitas. penyebab utama yaitu intake nutrisi pada manusia yang sangat berlebih dibanding pengeluarannya (energi yang digunakan). Sedangkan penyebab lain diantaranya : karena kelainan neuroendokrine dan sistem metabolik yang merupakan sistem pengaturan intake energi, penyimpanannya dan pengeluarannya. Ada beberapa faktor yang menyebabkan seseorang berpeluang mengalami obesitas, diantaranya :

    Prinsipnya kelebihan energ yang tidak digunakan maka akan menimbukan penyimpakan energy dalam bentuk lemak sehingga nantinya akumulasi lemak berlebih dalam tubuh menjadi banyak

Genetik dan lingkungan

Obesitas cenderung banyak terjadi pada perempuan dibanding dengan laki-laki. Seseorang dengan gen tertentu akan memiliki makanan spesifik dan nutrisi yang juga spesifik. Sedangkan faktor lingkungan, seperti adanya budaya (faktor kultural) akan berefek pada komposisi makanan dan level dari aktifitas fisik.

Aktivitas fisik yang kurang, dan komposisi makanan yang banyak mengandung lemak, maka akan menjadi faktor risiko utama terjadinya obesitas. Selain itu makanan lain juga bisa, karena prinsipnya kelebihan energ yang tidak digunakan maka akan menimbukan penyimpakan energy dalam bentuk lemak sehingga nantinya akumulasi lemak berlebih dalam tubuh menjadi banyak. Aktifitas yang kurang juga dapat menjadi penyebab obesitas, karena aktifitas yang kurang akan menyebabkan kekurangan pengeluaran energi, sehingga penyimpanannya menjadi besar

Pada anak-anak, biasanya obesitas terjadi pada orang yang bermalas-malasan seperti menonton TV dan tidur berlebih setelah makan. Sehingga aktifitas tersebut hendaknya di hindari dengan mengisinya dengan aktivitas yang baik agar pemakaian energi menjadi optimal sesuai yang dimasukkan.

Hipotiroidisme

Seseorang dengan kondisi penyakit hypothyroid maka akan cenderung terkena obesitas. seperti yang telah diketahui bersama, jika hipothiroidism dapat di ukur dengan melihat TSH (Thiroid Stimulating Hormon). Berdasarkan data yang ada, seseorang dengan hipotiroidism dengan mixedema, maka berat badannya cenderung naik.

Sindrome Cushing's

Pada obesitas, biasanya disertai dengan naiknya cortisol dan naiknya metabolit urin. Tidak seperti pada syndrome chusing, dimana level cortisol darah dan urin pada kondisi basal dan akibat respon dari coticotropin releasing hormone (CRH) dan ACTH cenderung normal. Obesitas di duga kuat berhubungan dengan reaktivasi local berlebihan dari kortisol pada lemak oleh 11beta-hydroxysteroid dehydrogenase 1, sebagai enzim yang mengubah bentuk inaktif cortisone menjadi cortisol.

    Ternyata tumor, trauma, inflamasi, disfungsi hipotalamus yang mengontrol mekanisme lapar, kenyang dan energi dapat menentukan derajat obesitas seseorang

Insulinoma

Insulinoma, suatu kondisi dimana terdapat tumor yang menyebabkan peningkatan produksi insulin tubuh, sering bermanifestasi pada naiknya berat badan yang merupakan akibat kompensasi gejala hipoglikemik. Naiknya kadar insulin dalam darah, akan menimbulkan penyimpanan energi menjadi lemak meningkat. Kondisi seperti ini dapat ditemui pada berbagai tipe orang, tetapi kebanyakannya obesitas dengan penyebab ini lebih sulit dideteksi.

