            	<h2>Mengatasi Diare Pada Anak</h2>
                <img src="images/baby.jpg" align="left" style="padding-right:20px; padding-top:5px;" />
                <p>Ketika orangtua atau keluarga mendapati anak mengalami diare, maka yang perlu diperhatikan selain dari keadaan feses dan frekuensi diarenya adalah keadaan fisik anak. Pada umumnya, seorang anak yang mengalami diare akan kehilangan banyak cairan tubuh. Sehingga ia akan cenderung mengalami dehidrasi (kondisi ketidakseimbangan cairan tubuh). Apabila kondisi ini tidak terdeteksi dengan tepat maka ada kekhawatiran timbulnya masalah yang lebih serius pada anak, terlebih jika usianya masih sangat muda.Pemberian manajemen (penanganan) pada anak dengan diare didasarkan pada klasifikasi tingkat dehidrasi yang dialami. Hal ini dibagi dalam beberapa kelompok, yaitu : dehidrasi berat, dehidrasi ringan/sedang, dan tanpa dehidrasi.

Ciri-ciri dari ketiga kondisi tersebut adalah :

    Dehidrasi berat

Apabila terdapat 2 atau lebih gejala berikut : letargi (tidak sadar), mata cekung, tidak mau atau malas minum, cubitan perut kembali dalam waktu lama (≥2 detik).

    Dehidrasi ringan/sedang

Apabila terdapat 2 atau lebih gejala berikut : rewel atau gelisah, mata cekung, minum dengan lahap, dan cubitan kembali dengan lambat (<=2 detik)

    Tanpa dehidrasi

Apabila tidak terdapat tanda dehidrasi berat maupun sedang/ringan.

 

Berdasarkan pengklasifikasian ini, orangtua bisa mengenali kondisi anak dan dapat memberikan penanganan yang tepat.
Apabila anak didapati mengalami diare dengan dehidrasi berat, sebaiknya anak segera dibawa ke rumah sakit atau dokter untuk mendapatkan penanganan infus (memberikan nutrisi melalui jalur pembuluh darah, yang biasa dipasang di pergelangan tangan) segera.

 

Apabila anak, maka orangtua bisa melakukan perawatan secara mandiri dengan melakukan mengalami diare dengan disertai dehidrasi ringan/ sedang pemantauan secara terus menerus.

 

Dalam penanganannya, orangtua bisa melakukan beberapa hal berikut ini, yaitu :

-                    Berikan cairan oralit dengan jumlah yang cukup pada 3 jam pertama

    Jumlah oralit yang diberikan didasarkan pada berat badan atau umur si anak. Namun ini hanya menjadi acuan dasar minimal yang harus dipenuhi. Jadi apabila anak masih mau minum sebaiknya ia diberikan tambahan minuman lagi.
    Jumlah cairan oralit sebesar 75ml/kg BB anak selama 3 jam pertama
    Pembuatan larutan oralit yaitu dengan mencampurkan 1 sachet/bungkus oralit yang telah dibeli di apotek kemudian dicampur dengan air 200 ml.

-                    Evaluasi apabila ada hal-hal yang kurang wajar selama 3 jam pertama, antara lain:

    Anak mengalami muntah
    Ada pembengkakan pada kelopak mata
    Anak sudah enggan untuk minum

-                    Evaluasi kondisi anak pada 3 jam pertama,

    Apabila anak didapati masih dalam keadaaan dehidrasi ringan/sedang, maka lanjutkan terapi dalam masa 3 jam berikutnya dengan diberi tambahan makanan pendukung yang lain (misal : ASI, susu, jus, sup, dll). Namun perlu juga untuk dilakukan pemantauan lebih intensif terhadap perkembangan kondisi anak.
    Apabila anak membaik dan beralih pada keadaan tanpa dehidrasi, maka lakukan penanganan lanjutan sebagaimana penanganan keadaan diare tanpa dehidrasi (akan dijelaskan di bawah)
    Apabila kondisi anak semakin memburuk, beralih pada kondisi dehidrasi berat, maka segera bawa anak ke rumah sakit.

-                    Didasarkan pada hasil evaluasi yang telah dilakukan, baik dari kondisi dehidrasi maupun tanda kekhawatiran di atas, maka sebaiknya orangtua segera mengambil keputusan untuk membawa anak ke rumah sakit.

Akan tetapi apabila dirasa menyulitkan, maka dianjurkan anak langsung dibawa ke rumah sakit atau dokter karena dikhawatirkan kondisi memburuk dan beralih menjadi dehidrasi berat.

 

 

Apabila anak mengalami diare tanpa disertai dehidrasi, maka penanganan cukup dilakukan di rumah. Beberapa hal yang perlu diperhatikan dalam penanganannya adalah sebagai berikut :

    Beri cairan tambahan (ketika anak masih mau minum)

    Apabila anak masih dalam masa menyusui, perpanjang waktu menyusui
    Apabila anak masih berada pada masa ASI eksklusif (ASI yang diberikan secara murni tanpa tambahan bahan makanan apapun, yaitu ketika anak berusia kurang dari 6 bulan), maka berikan cairan oralit atau air matang sebagai tambahan.
    Jika anak tidak mendapatkan ASI eksklusif, berikan 1 atau lebih tambahan cairan berikut : oralit, cairan makanan (tajin, sup, kuah sayur, dll), atau air matang
    Pembuatan larutan oralit adalah dengan mencampurkan 1 bungkus oralit dengan air 200 ml (1 gelas sedang)
    Larutan oralit yang diberikan sebanyak
        ± 50–100 ml setiap kali anak BAB pada anak kurang dari 2 tahun
        ±100–200 ml setiap kali anak BAB pada usia lebih dari 2 tahun
        Pemberian minum dilakukan sedikit demi sedikit dengan menggunakan cangkir atau sendok (sesuai dengan kondisi dan usia anak)
        Jika anak mengalami muntah, maka hentikan terlebih dahulu selama 10 menit dan lanjutkan pemberian minum lagi dengan lebih lambat.
        Pemberian cairan tambahan dilakukan hingga anak tidak lagi mengalami diare.

    b.          Beri tambahan tablet Zinc

    Tablet Zinc diberikan selama 10 hari dengan ketentuan berikut :
        Anak usia kurang dari 6 bulan, berikan  tablet (10 mg)
        Anak usia lebih dari 6 bulan, berikan 1 tablet (20 mg)
        Pemberian tablet zinc bisa dilakukan dengan cara melarutkan dalam minuman anak atau dikunyah pada anak yang sudah lebih dewasa.

    c.           Lanjutkan pemberian makan/ASI

    Pada kondisi diare, makanan/ASI sebaiknya tetap diberikan sebagaimana biasanya karena ini akan turut memberikan sumbangsih terhadap nutrisi anak.

    Berkunjung kembali ke rumah sakit atau dokter, apabila ditemui tanda-tanda berikut :

    Anak tidak bisa atau malas minum atau menyusu
    Kondisi anak memburuk
    Anak demam
    Terdapat darah dalam fesesnya.</p><br /><br />

